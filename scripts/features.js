let featureappendfn = (data,a,b) => {
    let featureDiv=document.getElementById("featureDiv");
    featureDiv.innerHTML=null;

    let featureDiv1=document.createElement("div");
    featureDiv1.setAttribute("id","featureDiv1");
    featureDiv1.setAttribute("class","feature");

    let featureDiv2=document.createElement("div");
    featureDiv2.setAttribute("id","featureDiv2");
    featureDiv2.setAttribute("class","feature");   

    featureDiv.style.gridTemplateColumns="60% 22%";
    featureDiv.style.gap="20px";

    // first news div

    let h1=document.createElement("h1");
    h1.style.paddingLeft="16px";
    h1.setAttribute("id","featurehead1")
    h1.innerText=a;
    featureDiv1.append(h1);
   

    data.forEach( (el) => {
        let image=document.createElement("img");
        image.src=el.image;

        let subdiv=document.createElement("div");

        let title=document.createElement("h1");
        title.innerText=el.title;
        title.style.fontSize="16px";

        let summary=document.createElement("p");
        summary.innerText=el.summary;

        let author=document.createElement("p");
        author.innerText=el.author;

        let div=document.createElement("div");

        subdiv.append(title,summary,author);
        div.append(image,subdiv);

        featureDiv1.append(div);
    });

    // second news div

    let h2=document.createElement("h3");
    h2.style.paddingLeft="16px";
    h2.setAttribute("id","featurehead1")
    h2.innerText=b;
    featureDiv2.append(h2);

    for(let i=data.length-1;i>=0;i--){
        let el;
        if(i==9){
            let image=document.createElement("img");
            image.src="https://tpc.googlesyndication.com/simgad/11857604588559191702?";
            image.style.width="300px";

            image.style.display='flex';
            image.style.margin='auto';
            image.style.marginTop="20px";
            image.style.marginBottom='20px';
            featureDiv2.append(image);
            continue;
            
        }else{
            el=data[i];
        }
        
        let image=document.createElement("img");
        image.src=el.image;
        image.style.width="66px";
        image.style.height='63px';
        image.style.borderRadius="10%";  

        let subdiv=document.createElement("div");

        let title=document.createElement("h1");
        title.innerText=el.title;
        title.style.fontSize="15px";
        title.style.color='#2b2c2d';

        let author=document.createElement("p");
        author.innerText=el.author;

        let div=document.createElement("div");
        div.style.height='auto';
        div.style.padding="8px 16px";

        subdiv.append(title,author);
        div.append(image,subdiv);

        featureDiv2.append(div);
    };
    featureDiv.append(featureDiv1,featureDiv2);
}

